# Human-readable display names for packs
_PACK_DISPLAY_NAMES: dict[str, str] = {
    "core": "Build lifecycle",
    "quality": "Code quality",
    "career": "Career pipeline",
    "analyze": "Analysis engine",
    "domains": "Domain builders",
    "meta": "Workflow orchestration",
    "security": "Security analysis",
    "ds-project": "Project lifecycle",
    "ds-workorder": "Work order lifecycle",
    "ds-milestone": "Milestone lifecycle",
    "website": "Website builder",
    "fullstack": "Fullstack builder",
    "setup": "Setup",
}


def _build_routing_table(
    canonical_root: Path,
    packs_yaml_path: Path,
) -> str:
    """Generate the Pack-Based Routing table markdown dynamically from packs.yaml.

    Returns the full table as a markdown string.
    Falls back to empty table with a warning if packs.yaml cannot be read.
    """
    try:
        import yaml as _yaml
    except ImportError:
        warnings.warn("PyYAML not available; routing table will be empty", stacklevel=2)
        return _empty_routing_table()

    try:
        raw = packs_yaml_path.read_text(encoding="utf-8")
        data = _yaml.safe_load(raw)
    except (OSError, Exception) as exc:
        warnings.warn(f"Cannot read packs.yaml: {exc}; routing table will be empty", stacklevel=2)
        return _empty_routing_table()

    packs = data.get("packs", {})
    if not packs:
        return _empty_routing_table()

    header = (
        "### Pack-Based Routing\n\n"
        'Each pack is one skill with modes. Invoke via `Skill(skill="ds-<pack>", args="<mode>")`. '
        "If the user's message matches a keyword, invoke the pack and let the router infer the mode.\n\n"
        "| Pack | Skill | Mode keywords |\n"
        "|------|-------|---------------|\n"
    )

    rows: list[str] = []
    for pack_key, pack_cfg in packs.items():
        if not isinstance(pack_cfg, dict):
            continue

        # Resolve skill ID — prefer explicit 'skill' field
        skill_id = pack_cfg.get("skill", pack_key)
        # Prefix with ds- if not already prefixed
        if not skill_id.startswith("ds-"):
            skill_id = f"ds-{skill_id}"

        # Display name
        display_name = _PACK_DISPLAY_NAMES.get(pack_key, pack_key.replace("-", " ").title())

        # Mode keywords — try metadata.yml triggers first, fall back to mode name
        modes: list[str] = pack_cfg.get("modes", [])
        skill_path_override = pack_cfg.get("skill_path")
        keyword_parts = _build_mode_keywords(
            modes=modes,
            pack_key=pack_key,
            skill_path_override=skill_path_override,
            canonical_root=canonical_root,
        )

        rows.append(f"| {display_name} | `{skill_id}` | {keyword_parts} |")

    return header + "\n".join(rows) + "\n"


def _read_mode_triggers(
    mode: str, pack_key: str, skill_path_override: str | None, canonical_root: Path
) -> list[str] | None:
    """Return triggers list from metadata.yml for a mode, or None if not found."""
    # Determine base skill directory
    if skill_path_override:
        skill_dir = canonical_root.parent / skill_path_override
    else:
        skill_dir = canonical_root / "skills" / pack_key

    metadata_path = skill_dir / "modes" / mode / "metadata.yml"
    if not metadata_path.is_file():
        return None

    try:
        import yaml as _yaml

        data = _yaml.safe_load(metadata_path.read_text(encoding="utf-8"))
        raw = (data or {}).get("triggers", [])
        # triggers: list of "keyword:" strings — strip trailing colon
        triggers = []
        for item in raw:
            if isinstance(item, str):
                triggers.append(item.rstrip(":").strip())
            elif isinstance(item, dict):
                # YAML may parse "keyword:" as a dict key with None value
                for k in item:
                    triggers.append(str(k).rstrip(":").strip())
        return triggers if triggers else None
    except Exception:
        return None


def _read_pack_triggers(
    pack_key: str, skill_path_override: str | None, canonical_root: Path
) -> list[str] | None:
    """Return triggers list from pack-level metadata.yml, or None if not found."""
    if skill_path_override:
        skill_dir = canonical_root.parent / skill_path_override
    else:
        skill_dir = canonical_root / "skills" / pack_key

    metadata_path = skill_dir / "metadata.yml"
    if not metadata_path.is_file():
        return None

    try:
        import yaml as _yaml

        data = _yaml.safe_load(metadata_path.read_text(encoding="utf-8"))
        raw = (data or {}).get("triggers", [])
        triggers = []
        for item in raw:
            if isinstance(item, str):
                triggers.append(item.rstrip(":").strip())
            elif isinstance(item, dict):
                for k in item:
                    triggers.append(str(k).rstrip(":").strip())
        return triggers if triggers else None
    except Exception:
        return None


def _build_mode_keywords(
    modes: list[str],
    pack_key: str,
    skill_path_override: str | None,
    canonical_root: Path,
) -> str:
    """Build mode keyword string for routing table row.

    For each mode, reads metadata.yml triggers if present; falls back to mode name.
    Returns a ' · '-separated string of '**mode:** trigger1, trigger2' entries.
    For packs with no modes, reads pack-level metadata.yml triggers instead.
    """
    if not modes:
        triggers = _read_pack_triggers(pack_key, skill_path_override, canonical_root)
        if triggers:
            return ", ".join(f"{t}:" for t in triggers)
        return f"{pack_key}:"

    parts: list[str] = []
    for mode in modes:
        triggers = _read_mode_triggers(mode, pack_key, skill_path_override, canonical_root)
        if triggers:
            keyword_str = ", ".join(f"{t}:" for t in triggers)
            parts.append(f"**{mode}:** {keyword_str}")
        else:
            parts.append(f"**{mode}:** {mode}:")
    return " · ".join(parts)


def _empty_routing_table() -> str:
    return (
        "### Pack-Based Routing\n\n"
        "| Pack | Skill | Mode keywords |\n"
        "|------|-------|---------------|\n"
    )


def _inject_routing_table(projection_content: str, routing_table: str) -> str:
    """Replace content between <!-- BEGIN AUTO-ROUTING --> and <!-- END AUTO-ROUTING --> markers."""
    begin = _ROUTING_BEGIN
    end = _ROUTING_END

    begin_idx = projection_content.find(begin)
    end_idx = projection_content.find(end)

    if begin_idx == -1 or end_idx == -1:
        # Markers not found — append the table
        return projection_content + f"\n{begin}\n{routing_table}\n{end}\n"

    # Replace content between markers (inclusive of the end marker)
    before = projection_content[: begin_idx + len(begin)]
    after = projection_content[end_idx:]
    return before + "\n" + routing_table + after


def _build_claude_md(
    adapter_projection_path: Path,
    canonical_root: Path,
    packs_yaml_path: Path,
) -> str:
    """Return enforcement block prepended to adapter projection with dynamic routing table."""
    projection = ""
    if adapter_projection_path.is_file():
        projection = adapter_projection_path.read_text(encoding="utf-8")

    routing_table = _build_routing_table(canonical_root, packs_yaml_path)
    projection = _inject_routing_table(projection, routing_table)
    return _ENFORCEMENT_BLOCK + "\n" + projection


def compile_pack(canonical_root: Path | None = None) -> dict[str, Any]:
    """Read canonical/ and produce the Claude Code integration pack.

    Returns a deterministic dict:
    {
        "tool": "claude_code",
        "canonical_root": str,
        "files": {
            "skills/ds-bootstrap/SKILL.md": "<content>",
        },
        "settings_hooks": [...],  # hook entries for settings.json merge
    }

    Raises FileNotFoundError if canonical/skills/ds-bootstrap/SKILL.md is missing.
    """
    root = canonical_root if canonical_root is not None else _REPO_ROOT / "canonical"

    skill_md_path = root / "skills" / "ds-bootstrap" / "SKILL.md"
    if not skill_md_path.is_file():
        raise FileNotFoundError(
            f"canonical/skills/ds-bootstrap/SKILL.md not found at {skill_md_path}"
        )
    skill_md_content = skill_md_path.read_text(encoding="utf-8")
    claude_md_content = _build_claude_md(
        _ADAPTER_PROJECTION,
        canonical_root=root,
        packs_yaml_path=_PACKS_YAML,
    )

    hooks_template = _load_hooks_template()

    return {
        "tool": "claude_code",
        "canonical_root": str(root),
        "files": {
            "skills/ds-bootstrap/SKILL.md": skill_md_content,
            "CLAUDE.md": claude_md_content,
        },
        "settings_hooks": hooks_template,
    }


def _load_hooks_template() -> list[dict[str, Any]]:
    if not _HOOKS_TEMPLATE.is_file():
        return []
    try:
        return json.loads(_HOOKS_TEMPLATE.read_text(encoding="utf-8"))
    except (json.JSONDecodeError, OSError):
        return []

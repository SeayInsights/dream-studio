import warnings
from pathlib import Path
from typing import Any

_COMPILER_DIR = Path(__file__).resolve().parent
_INTEGRATIONS_DIR = _COMPILER_DIR.parent
_REPO_ROOT = _INTEGRATIONS_DIR.parent
_HOOKS_TEMPLATE = _INTEGRATIONS_DIR / "targets" / "claude_code" / "hooks_template.json"
_ADAPTER_PROJECTION = _REPO_ROOT / "adapter-projections" / "claude" / "CLAUDE.md"
_PACKS_YAML = _REPO_ROOT / "packs.yaml"

_ROUTING_PLACEHOLDER = "<!-- ROUTING TABLE GENERATED BY COMPILER -->"
_ROUTING_BEGIN = "<!-- BEGIN AUTO-ROUTING -->"
_ROUTING_END = "<!-- END AUTO-ROUTING -->"


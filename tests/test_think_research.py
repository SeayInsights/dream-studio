def generate_spec_md_with_research(report: web_research.ResearchReport) -> str:
    """Generate a minimal spec.md with research findings section.

    This simulates what the think mode would output when --research flag is used.

    Args:
        report: ResearchReport to include in spec

    Returns:
        Markdown content for spec.md file
    """
    confidence_label = (
        "High" if report.confidence > 0.75 else "Medium" if report.confidence >= 0.5 else "Low"
    )

    # Add unverified warning for low confidence
    warning = ""
    if report.confidence < 0.6:
        warning = " ⚠️ **UNVERIFIED** - Low confidence, proceed with caution"

    # Note: report.findings already includes "## Research Findings" header and tier sections
    # We just need to add the metadata (topic, confidence, triangulation) before the tier sections

    # Extract tier sections from findings (skip the header)
    findings_content = report.findings
    if findings_content.startswith("## Research Findings"):
        # Remove the header and extract just the tier sections
        findings_content = findings_content.replace("## Research Findings\n", "", 1)

    spec_content = f"""# Feature Specification

## Overview
This is a test specification document.

## User Stories
- **P1:** User can perform basic task
- **P2:** User can configure advanced options

## Functional Requirements
- **FR-001:** System MUST implement core functionality
- **FR-002:** System MUST validate user input

## Decision Rationale
We chose approach A because of performance benefits.

## Research Findings

**Topic:** {report.topic}
**Confidence:** {report.confidence:.2f} ({confidence_label}){warning}
**Triangulation:** {report.triangulation:.2f} ({len(report.sources)} sources)
{findings_content}

## Success Criteria
- **SC-001:** Feature completes in <500ms
- **SC-002:** 95% user task completion rate
"""

    return spec_content


